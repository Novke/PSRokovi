    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        // U JEDNOM TERMINU SE NA JEDNOJ STANICI MOZE EMITOVATI SAMO JEDNA EMISIJA
        // NISAM URADIO DA NE PREPLAVIM KOD
        // UGLAVNOM, SALJE SE ZAHTEV SERVERU, VRACAJU SE SVE EMISIJE
        // I UPREDJUJU SE DATUMI I DA LI POSTOJI TA STANICA VEC U TOM TERMINU,
        // MALO DUZI PROCES
        
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date dan = sdf.parse(txtDan.getText());
            String opis = txtOpis.getText();
            ModelTabeleStavke mt = (ModelTabeleStavke) tblStavke.getModel();
            
            if(mt.getLista().isEmpty()){
                JOptionPane.showMessageDialog(this, "U vodicu mora postojati barem jedna emisija!");
                return;
            }

            ProgramskiVodic pv = new ProgramskiVodic(-1, dan, opis, urednik.getUsername(), mt.getLista());

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ_PROGRAMSKI_VODIC);
            kz.setParametar(pv);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            boolean uspesno = (boolean) so.getOdgovor();

            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
            } else {
                JOptionPane.showMessageDialog(this, "Doslo je do greske...");
            }

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

