    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtAdresa.getText().isEmpty() || txtBrojLista.getText().isEmpty()
                || txtDatum.getText().isEmpty() || txtGodinaIzgradnje.getText().isEmpty()
                || txtMestoNepokretnosti.getText().isEmpty() || txtPovrsina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }

        try {

            String adresa = txtAdresa.getText();
            String mestoNepokretnosti = txtMestoNepokretnosti.getText();
            boolean mestoPrebivalista = rbDa.isSelected();

            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblPrijave.getModel();
            if (mestoPrebivalista && mt.vecImaTrue()) {
                JOptionPane.showMessageDialog(this, "Vec ima prijava sa TRUE mestom prebivalista!");
                return;
            }

            int brojListaNepokretnosti = Integer.parseInt(txtBrojLista.getText());
            int godinaIzgradnje = Integer.parseInt(txtGodinaIzgradnje.getText());

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumKoriscenja = sdf.parse(txtDatum.getText());
            
            Calendar calendar = Calendar.getInstance();
            calendar.setTime(datumKoriscenja);
            
            if(godinaIzgradnje > calendar.get(Calendar.YEAR)){
                JOptionPane.showMessageDialog(this, "Godina izgradnje mora biti u toku "
                        + "ili pre godine pocetka koriscenja!");
                return;
            }

            double povrsina = Double.parseDouble(txtPovrsina.getText());
            
            if(povrsina <= 0){
                JOptionPane.showMessageDialog(this, "Povrsina mora biti veca od nule!");
                return;
            }
            
            VrstaNepokretnosti vn = (VrstaNepokretnosti) cmbVrstaNepokretnosti.getSelectedItem();

            PoreskaPrijava pp = new PoreskaPrijava(-1, adresa, mestoNepokretnosti,
                    mestoPrebivalista, brojListaNepokretnosti, godinaIzgradnje,
                    datumKoriscenja, povrsina, new Date(), po, vn);

            mt.dodajPrijavu(pp);

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblPrijave.getSelectedRow();

        if (selectedRow >= 0) {
            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblPrijave.getModel();
            mt.obrisiPrijavu(selectedRow);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblPrijave.getModel();
        ArrayList<PoreskaPrijava> prijave = mt.getLista();

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.SACUVAJ, prijave);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
        } else {
            JOptionPane.showMessageDialog(this, "Doslo je do greske prilikom cuvanja...");
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

