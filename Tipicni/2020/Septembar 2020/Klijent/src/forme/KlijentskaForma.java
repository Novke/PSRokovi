    private void popuniNeobradjeneZahteve() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.POPUNI_NEOBRADJENE_ZAHTEVE);
        kz.setParametar(laborant.getLaboratorija().getLaboratorijaId());

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Zahtev> neobradjeni = (ArrayList<Zahtev>) so.getOdgovor();

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblZahtevi.getModel();
        mt.dodajZahteve(neobradjeni);

    }

    void izmeniZahtev(Zahtev z) {

        z.setLaborant(laborant);
        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblZahtevi.getModel();
        mt.izmeniZahtev(z, tblZahtevi.getSelectedRow());

    }
