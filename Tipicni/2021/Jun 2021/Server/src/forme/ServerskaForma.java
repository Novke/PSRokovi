    public void izvrsiUpit() {

        ArrayList<PoreskiObveznik> listaBaza = new ArrayList<>();
        ModelTabeleServer mt = (ModelTabeleServer) tblObveznici.getModel();
        String dodatniUpit = "";

        if (rbImePrezime.isSelected() && !txtImePrezime.getText().isEmpty()) {
            String imePrezime = txtImePrezime.getText();
            dodatniUpit = " WHERE concat(ime, concat(' ', prezime)) like '%" + imePrezime.toLowerCase() + "%';";
            listaBaza = Kontroler.getInstance().vratiObveznike(dodatniUpit);
            mt.dodajListu(listaBaza);
        }

        if (rbJMBG.isSelected() && !txtJMBG.getText().isEmpty()) {
            String JMBG = txtJMBG.getText();
            dodatniUpit = " WHERE JMBG like '%" + JMBG + "%';";
            listaBaza = Kontroler.getInstance().vratiObveznike(dodatniUpit);
            mt.dodajListu(listaBaza);
        }

        if ((!rbImePrezime.isSelected() && !rbJMBG.isSelected())
                || (txtImePrezime.getText().isEmpty() || txtJMBG.getText().isEmpty())) {
            listaBaza = Kontroler.getInstance().vratiObveznike(dodatniUpit);
            mt.dodajListu(listaBaza);
        }

    }

