    private void btnEvidentirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvidentirajActionPerformed

        //NISAM ODRADIO SAMO VALIDACIJU JEDINSTVENOSTI NA STRANI SERVERA
        //DA NE KOMPLIKUJEM VEC ZAKOMPLIKOVANO (nije tesko, ali mora da se 
        //prodje kroz sve u bazi i proveri da li je isto, jednostavno smara)
        if (txtDatum.getText().isEmpty() || txtNazivKompanije.getText().isEmpty()
                || txtMaticni.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
            return;
        }

        String nazivKompanije = txtNazivKompanije.getText();
        String tipKompanije = (String) cmbTipKompanije.getSelectedItem();
        String maticni = txtMaticni.getText();
        boolean PDVObveznik;

        PDVObveznik = rbDa.isSelected();

        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");

        try {
            Date datum = sdf.parse(txtDatum.getText());

            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblZaposleni.getModel();
            ArrayList<Zaposleni> listaZaposlenih = mt.getLista();

            if (listaZaposlenih.size() < 1) {
                JOptionPane.showMessageDialog(this, "Mora da ima bar 1 zaposlenog!");
                return;
            }

            Kompanija k = new Kompanija(-1, nazivKompanije, tipKompanije, PDVObveznik,
                    maticni, datum, listaZaposlenih.size(), ulogovani, listaZaposlenih);

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ);
            kz.setParametar(k);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            boolean uspesno = (boolean) so.getOdgovor();

            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano.");
            } else {
                JOptionPane.showMessageDialog(this, "Nije uspesno sacuvano.");
            }

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnEvidentirajActionPerformed

