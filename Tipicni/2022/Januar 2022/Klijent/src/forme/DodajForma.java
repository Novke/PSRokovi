    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if(txtDatumObjave.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Datum mora biti popunjen!");
            return;
        }
        
        try {
            String platforma = (String) cmbPlatforma.getSelectedItem();
            String status = (String) cmbStatus.getSelectedItem();
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumObjave = sdf.parse(txtDatumObjave.getText());
            
            Dostupnost d = new Dostupnost(null, -1, platforma, datumObjave, status);
            
            KlijentskaForma kf = (KlijentskaForma) getParent();
            kf.dodajDostupnost(d);
            
        } catch (ParseException ex) {
            Logger.getLogger(DodajForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_btnDodajActionPerformed

