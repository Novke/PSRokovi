    private void popuniLokacije() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_LOKACIJE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Lokacija> lokacije = (ArrayList<Lokacija>) so.getOdgovor();

        cmbLokacija.removeAllItems();

        for (Lokacija lokacija : lokacije) {
            cmbLokacija.addItem(lokacija);
        }

    }

    void dodajVrstu(String vrstaVolontiranja) {

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblVrsteVolontiranja.getModel();
        boolean uspesno = mt.dodajVrstu(vrstaVolontiranja);

        if (!uspesno) {
            JOptionPane.showMessageDialog(this, "Vec je uneta ova vrsta volontiranja!");
            return;
        }

    }
