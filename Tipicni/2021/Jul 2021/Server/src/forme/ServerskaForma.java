    public void izvrsiUpit() {
        
        ArrayList<StavkaIzvestaja> listaBaza;
        ModelTabeleServer mt = (ModelTabeleServer) tblIzvestaj.getModel();
        String dodatniUpit = "";
        
        // uradio sam samo ovaj if, ostale znate jer ste radili bitnije rokove
        // sto se tice upita, racunanje trajanja sam kucao na guglu
        // moze i bolje da se uradi, ali nisam istrazivao dublje
        // jer nije toliko bitno. Ne znam kako mogu da daju ovakve
        // stvari koje niko na faksu nije pokazivao i za sta je potreban internet
        // ali dobro, sitnica
        
        if(!cbTakmicenje.isSelected() && !cbImePrezime.isSelected()){
            listaBaza = Kontroler.getInstance().vratiIzvestaj();
            mt.dodajIzvestaj(listaBaza);
        }
        
    }

