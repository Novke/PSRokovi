    private void popuniAktivneRadnikeVoditelje() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_AKTIVNE_RADNIKE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Radnik> aktivniRadnici = (ArrayList<Radnik>) so.getOdgovor();

        cmbVoditelj.removeAllItems();

        for (Radnik radnik : aktivniRadnici) {
            cmbVoditelj.addItem(radnik);
        }

    }

    private void popuniAktivneStudije() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_AKTIVNE_STUDIJE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Studio> aktivnaStudija = (ArrayList<Studio>) so.getOdgovor();

        cmbStudio.removeAllItems();

        for (Studio studio : aktivnaStudija) {
            cmbStudio.addItem(studio);
        }

    }

