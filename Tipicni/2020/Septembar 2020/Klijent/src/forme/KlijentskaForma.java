    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        int selectedRow = tblZahtevi.getSelectedRow();

        if (selectedRow >= 0) {
            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblZahtevi.getModel();
            Zahtev z = mt.vratiZahtev(selectedRow);

            IzmeniForma izf = new IzmeniForma(this, true, z);
            izf.setVisible(true);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblZahtevi.getModel();
        ArrayList<Zahtev> obradjeniZahtevi = mt.vratiObradjene();

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ);
        kz.setParametar(obradjeniZahtevi);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        boolean uspesno = (boolean) so.getOdgovor();
        
        if (uspesno) {
            popuniNeobradjeneZahteve();
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
        } else {
            JOptionPane.showMessageDialog(this, "Nije uspesno sacuvano...");
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

