    private void popuniPretplatnike() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_PRETPLATNIKE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Pretplatnik> pretplatnici = (ArrayList<Pretplatnik>) so.getOdgovor();

        cmbPretplatnik.removeAllItems();

        for (Pretplatnik pretplatnik : pretplatnici) {
            cmbPretplatnik.addItem(pretplatnik);
        }

    }

    private void popuniPakete() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_PAKETE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Paket> paketi = (ArrayList<Paket>) so.getOdgovor();

        cmbPaket.removeAllItems();

        for (Paket paket : paketi) {
            cmbPaket.addItem(paket);
        }

    }
