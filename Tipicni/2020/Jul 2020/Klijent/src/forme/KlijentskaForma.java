    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtTemperatura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste popunili temperaturu!");
            return;
        }

        double temperatura = Double.parseDouble(txtTemperatura.getText());

        if (temperatura > 50 || temperatura < -50) {
            JOptionPane.showMessageDialog(this, "Temperatura mora biti izmedju -50 i 50!");
            return;
        }

        Region region = (Region) cmbRegion.getSelectedItem();
        //da vidimo da li je vec uneta prognoza za ovaj region
        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblPrognoza.getModel();

        if (mt.unetaPrognozaZaRegion(region)) {
            JOptionPane.showMessageDialog(this, "Vec je uneta prognoza za ovaj region!");
            return;
        }

        String meteoAlarm = (String) cmbMeteoalarm.getSelectedItem();
        String pojava = (String) cmbPojava.getSelectedItem();

        PrognozaRegion pr = new PrognozaRegion(new Prognoza(), -1,
                temperatura, meteoAlarm, pojava, region);

        mt.dodajPrognozuZaRegion(pr);

    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int selectedRow = tblPrognoza.getSelectedRow();

        if (selectedRow >= 0) {
            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblPrognoza.getModel();
            mt.obrisi(selectedRow);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        if (txtDan.getText().isEmpty() || txtOpis.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Dan i opis moraju biti popunjeni!");
            return;
        }

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblPrognoza.getModel();
        // proveravamo da li je za svaki region uneta prognoza
        // broj prognoza za region u tabeli mora biti isti kao broj regiona u bazi
        // odnosno comboboxu
        if (mt.getRowCount() != cmbRegion.getItemCount()) {
            JOptionPane.showMessageDialog(this, "Za svaki region mora biti uneta prognoza!");
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");

        try {
            Date dan = sdf.parse(txtDan.getText());
            String opis = txtOpis.getText();

            ArrayList<PrognozaRegion> prognozeZaRegion = mt.getListaPrognozaZaRegion();

            Prognoza prognoza = new Prognoza(-1, dan, opis, m, prognozeZaRegion);

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ);
            kz.setParametar(prognoza);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            boolean uspesno = (boolean) so.getOdgovor();

            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
            } else {
                JOptionPane.showMessageDialog(this, "Nije uspesno sacuvano!");
            }

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

