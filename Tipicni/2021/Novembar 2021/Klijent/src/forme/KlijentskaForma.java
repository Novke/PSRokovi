    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        
        try {
            if(txtNazivEmisije.getText().isEmpty() || txtDatumPocetka.getText().isEmpty()
                    || txtDatumKraja.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }
            
            String nazivEmisije = txtNazivEmisije.getText();
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
            
            Date datumVremePocetka = sdf.parse(txtDatumPocetka.getText());
            Date datumVremeKraja = sdf.parse(txtDatumKraja.getText());
            
            if(datumVremePocetka.after(datumVremeKraja)){
                JOptionPane.showMessageDialog(this, "Datum i vreme pocetka mora biti pre datuma "
                        + "i vremena kraja!");
                return;
            }
            
            Radnik voditelj = (Radnik) cmbVoditelj.getSelectedItem();
            Studio studio = (Studio) cmbStudio.getSelectedItem();
            String nacinEmitovanja = (String) cmbNacinEmitovanja.getSelectedItem();
            
            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblAngazovanja.getModel();
            
            Emisija emisija = new Emisija(-1, nazivEmisije, datumVremePocetka, 
                    datumVremeKraja, nacinEmitovanja, producent.getUsername(), 
                    voditelj, studio, mt.getLista());
            
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ);
            kz.setParametar(emisija);
            
            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            
            boolean uspesno = (boolean) so.getOdgovor();
            
            if(uspesno){
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
            }else{
                JOptionPane.showMessageDialog(this, "Nije uspesno...");
            }
            
        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

