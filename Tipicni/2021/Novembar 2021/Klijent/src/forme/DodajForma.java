    private void popuniAktivneRadnike() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_AKTIVNE_RADNIKE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Radnik> aktivniRadnici = (ArrayList<Radnik>) so.getOdgovor();

        cmbRadnik.removeAllItems();

        for (Radnik radnik : aktivniRadnici) {
            cmbRadnik.addItem(radnik);
        }

    }
