    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        
        if(!radioDa.isSelected() && !radioNe.isSelected()){
            JOptionPane.showMessageDialog(this, "Niste obelezili obavezno prusustvo.");
            return;
        }
        
        if(txtOpis.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Niste uneli opis!");
            return;
        }
        
        Korisnik k = (Korisnik) cmbUcesnik.getSelectedItem();
        String opisUcesca = txtOpis.getText();
        boolean obaveznoPrisustvo = radioDa.isSelected();
        
        Ucesce u = new Ucesce(null, -1, opisUcesca, obaveznoPrisustvo, k);
        
        KlijentskaForma kf = (KlijentskaForma) getParent();
        kf.dodajUcesce(u);
        
    }//GEN-LAST:event_btnDodajActionPerformed

