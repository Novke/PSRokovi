package niti;

import domen.Paket;
import domen.Pretplatnik;
import domen.Ugovor;
import java.io.IOException;
import java.io.ObjectInputStream;
import java.io.ObjectOutputStream;
import java.net.Socket;
import java.util.ArrayList;
import java.util.logging.Level;
import java.util.logging.Logger;
import konstante.Operacije;
import logika.Kontroler;
import transfer.KlijentskiZahtev;
import transfer.ServerskiOdgovor;


public class ObradaKlijentskihZahteva extends Thread{
    private Socket s;

    public ObradaKlijentskihZahteva(Socket s) {
        this.s = s;
    }

    @Override
    public void run() {
        while(true){
            KlijentskiZahtev kz = primiZahtev();
            ServerskiOdgovor so = new ServerskiOdgovor();
            switch(kz.getOperacija()){
                case Operacije.VRATI_PRETPLATNIKE:
                    ArrayList<Pretplatnik> pretplatnici = Kontroler.getInstance().vratiPretplatnike();
                    so.setOdgovor(pretplatnici);
                    break;
                case Operacije.VRATI_PAKETE:
                    ArrayList<Paket> paketi = Kontroler.getInstance().vratiPakete();
                    so.setOdgovor(paketi);
                    break;
                case Operacije.SACUVAJ:
                    ArrayList<Ugovor> listaUgovora = (ArrayList<Ugovor>) kz.getParametar();
                    boolean uspesno = Kontroler.getInstance().sacuvajUgovore(listaUgovora);
                    so.setOdgovor(uspesno);
                    break;
            }
            posaljiOdgovor(so);
        }
    }

    private KlijentskiZahtev primiZahtev() {
        try {
            ObjectInputStream ois = new ObjectInputStream(s.getInputStream());
            return (KlijentskiZahtev) ois.readObject();
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(ObradaKlijentskihZahteva.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    private void posaljiOdgovor(ServerskiOdgovor so){
        try {
            ObjectOutputStream oos = new ObjectOutputStream(s.getOutputStream());
            oos.writeObject(so);
            oos.flush();
        } catch (IOException ex) {
            Logger.getLogger(ObradaKlijentskihZahteva.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
}
