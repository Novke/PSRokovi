    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try{
            String name=txtKorsinickoIme.getText();
            boolean uspeh=KlijentController.getInstance().login(name);
            if(!uspeh){
                JOptionPane.showMessageDialog(this, "Neuspesan login gasenje programa");
                System.exit(0);
            }
            btnLogin.setEnabled(false);
            txtKorsinickoIme.setEditable(false);
            txtOdgovor.setEditable(true);
            ThreadForm thread=new ThreadForm(this);
            thread.start();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnPosaljiOdgovorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiOdgovorActionPerformed
        try{
            String txtOdg=txtOdgovor.getText();
            Pitanje pitanje=new Pitanje(trenutnoPitanje.getPitanjeId(), trenutnoPitanje.getPitanje(),txtOdg, trenutnoPitanje.getBrojPoena());
            String poruka=KlijentController.getInstance().posaljiOdgovor(pitanje);
            txtPoruka.setText(poruka);
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnPosaljiOdgovorActionPerformed

