    private void popuniKomponente() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.POPUNI_KOMPONENTE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Komponenta> komponente = (ArrayList<Komponenta>) so.getOdgovor();

        cmbKomponenta.removeAllItems();

        for (Komponenta komponenta : komponente) {
            cmbKomponenta.addItem(komponenta);
        }
    }
