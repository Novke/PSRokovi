    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtDatum.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli datum!");
            return;
        }

        Pretplatnik pretplatnik = (Pretplatnik) cmbPretplatnik.getSelectedItem();

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblUgovori.getModel();
        if (mt.postojiPretplatnik(pretplatnik.getPretplatnikID())) {
            JOptionPane.showMessageDialog(this, "Pretplatnik vec ima ugovor!");
            return;
        }

        Paket paket = (Paket) cmbPaket.getSelectedItem();

        if (!paket.isAktivan()) {
            JOptionPane.showMessageDialog(this, "Paket nije aktivan!");
            return;
        }

        String vrstaUgovora = (String) cmbVrstaUgovora.getSelectedItem();

        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");

        try {
            Date datumEvidentiranja = sdf.parse(txtDatum.getText());

            boolean dodatneUsluge;

            if (rbDa.isSelected()) {
                dodatneUsluge = true;
            } else {
                dodatneUsluge = false;
            }

            Ugovor u = new Ugovor(-1, datumEvidentiranja, vrstaUgovora, dodatneUsluge,
                    paket.getCena(), paket, pretplatnik);

            if ((vrstaUgovora.equals("Bez ugovorne obaveze") && !dodatneUsluge)
                    || (!vrstaUgovora.equals("Bez ugovorne obaveze") && dodatneUsluge)) {
                u.setCena(u.getCena() * 1.1);
            }

            if (vrstaUgovora.equals("Bez ugovorne obaveze") && dodatneUsluge) {
                u.setCena(u.getCena() * 1.2);
            }

            mt.dodajUgovor(u);

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int selectedRow = tblUgovori.getSelectedRow();

        if (selectedRow >= 0) {
            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblUgovori.getModel();
            mt.obrisiUgovor(selectedRow);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblUgovori.getModel();
        ArrayList<Ugovor> listaUgovora = mt.getLista();

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ);
        kz.setParametar(listaUgovora);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        boolean uspesno = (boolean) so.getOdgovor();
        
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
        } else {
            JOptionPane.showMessageDialog(this, "Greska u cuvanju...");
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

