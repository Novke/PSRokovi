    private void btnPonistiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPonistiActionPerformed

        int selectedRow = tblIzvestaj.getSelectedRow();

        if (selectedRow >= 0) {
            ModelTabeleServer mt = (ModelTabeleServer) tblIzvestaj.getModel();
            Prijava p = mt.vratiPrijavu(selectedRow);

            boolean uspesno = Kontroler.getInstance().ponistiPrijavu(p);

            if (uspesno) {
                izvrsiUpit();
                JOptionPane.showMessageDialog(this, "Prijava je uspesno ponistena.");
            } else {
                JOptionPane.showMessageDialog(this, "Desila se greska pri ponistavanju prijave...");
            }
        }


    }//GEN-LAST:event_btnPonistiActionPerformed

