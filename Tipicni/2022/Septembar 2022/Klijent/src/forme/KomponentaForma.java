    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }

        Komponenta komponenta = (Komponenta) cmbKomponenta.getSelectedItem();
        int kolicina = Integer.parseInt(txtKolicina.getText());
        boolean kontrolisano = cbKontrolisano.isSelected();

        StavkaPrevoznogSredstva sps
                = new StavkaPrevoznogSredstva(null, -1, kolicina, kontrolisano, komponenta);

        KlijentskaForma kf = (KlijentskaForma) getParent();
        kf.dodajStavku(sps);

    }//GEN-LAST:event_btnDodajActionPerformed

