    void dodajAngazovanje(PredajePredmet pp) {

        pp.setKorisnik(korisnik);

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblAngazovanja.getModel();

        if (mt.nastavnikViseOdPet(pp)) {
            JOptionPane.showMessageDialog(this, "Nastavnik ne moze da bude angazovan na vise od "
                    + "5 predmeta!");
            return;
        }

        if (mt.nastavnikVecPredaje(pp)) {
            JOptionPane.showMessageDialog(this, "Nastavnik vec predaje ovaj predmet!");
            return;
        }

        mt.dodajAngazovanje(pp);

    }
