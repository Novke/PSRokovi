    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed

        int selectedRow = tblObveznici.getSelectedRow();
        
        if(selectedRow >= 0){
            ModelTabeleServer mt = (ModelTabeleServer) tblObveznici.getModel();
            PoreskiObveznik po = mt.vratiIzabranogObveznika(selectedRow);
            
            ArrayList<PoreskaPrijava> prijaveObveznika 
                    = Kontroler.getInstance().vratiPrijaveObveznika(po);
            
            if(prijaveObveznika.isEmpty()){
                JOptionPane.showMessageDialog(this, "Ovaj obveznik nema prijave!");
                return;
            }
            
            Detalji d = new Detalji(this, true, prijaveObveznika);
            d.setVisible(true);
            
        }

    }//GEN-LAST:event_btnDetaljiActionPerformed

