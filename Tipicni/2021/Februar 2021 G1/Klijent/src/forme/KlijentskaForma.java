    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        if (txtJMBG.getText().isEmpty() || txtIme.getText().isEmpty()
                || txtPrezime.getText().isEmpty() || txtElPosta.getText().isEmpty()
                || txtMobilni.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }

        String drzavljanstvo = (String) cmbDrzavljanstvo.getSelectedItem();
        String JMBG = txtJMBG.getText();

        if (JMBG.length() != 13) {
            JOptionPane.showMessageDialog(this, "JMBG mora imati 13 cifara!");
            return;
        }

        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String email = txtElPosta.getText();

		// nije bitno da radite validaciju za email jer je ovakva kakva je
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\."
                + "[a-zA-Z0-9_+&*-]+)*@"
                + "(?:[a-zA-Z0-9-]+\\.)+[a-z"
                + "A-Z]{2,7}$";

        Pattern pat = Pattern.compile(emailRegex);

        if (!pat.matcher(email).matches()) {
            JOptionPane.showMessageDialog(this, "Elektronska posta nije u validnom formatu!");
            return;
        }

        String mobilni = txtMobilni.getText();
        boolean specificnaOboljenja = false;

        if (rbDa.isSelected()) {
            specificnaOboljenja = true;
        }

        Opstina opstina = (Opstina) cmbOpstina.getSelectedItem();

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblStavka.getModel();
        ArrayList<StavkaPrijave> stavkePrijave = mt.getLista();

        if (stavkePrijave.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Prijava mora sadrzati barem jedan tip vakcine!");
            return;
        }

        Prijava p = new Prijava(-1, drzavljanstvo, JMBG, ime, prezime,
                email, mobilni, specificnaOboljenja, new Date(),
                opstina, stavkePrijave);

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ);
        kz.setParametar(p);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        boolean uspesno = (boolean) so.getOdgovor();
        
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
        } else {
            JOptionPane.showMessageDialog(this, "Greska pri cuvanju...");
        }


    }//GEN-LAST:event_btnSacuvajActionPerformed

