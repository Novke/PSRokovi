    private void popuniNepokretnosti() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_NEPOKRETNOSTI);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        ArrayList<VrstaNepokretnosti> nepokretnosti = (ArrayList<VrstaNepokretnosti>) so.getOdgovor();

        cmbVrstaNepokretnosti.removeAllItems();

        for (VrstaNepokretnosti vrstaNepokretnosti : nepokretnosti) {
            cmbVrstaNepokretnosti.addItem(vrstaNepokretnosti);
        }

    }
