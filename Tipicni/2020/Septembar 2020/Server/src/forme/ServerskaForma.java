    private void popuniLaboratorije() {

        ArrayList<Laboratorija> listaLaboratorija = Kontroler.getInstance().vratiLaboratorije();

        cmbLaboratorija.removeAllItems();

        for (Laboratorija laboratorija : listaLaboratorija) {
            cmbLaboratorija.addItem(laboratorija);
        }

    }
