    private void popuniIzdavace() {
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_IZDAVACE);
        
        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        
        ArrayList<Izdavac> izdavaci = (ArrayList<Izdavac>) so.getOdgovor();
        
        cmbIzdavac.removeAllItems();
        
        for (Izdavac izdavac : izdavaci) {
            cmbIzdavac.addItem(izdavac);
        }
        
    }

    void dodajDostupnost(Dostupnost d) {
        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblDostupnosti.getModel();
        
        if(mt.postojiDostupnostNaPlatformi(d)){
            JOptionPane.showMessageDialog(this, "Vec ste uneli dostupnost na " 
                    + d.getPlatforma() + " platformi!");
            return;
        }
        
        mt.dodajDostupnost(d);
    }

