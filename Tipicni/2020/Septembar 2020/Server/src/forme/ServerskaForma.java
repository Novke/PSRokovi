    public void izvrsi() {

        String upit = "";
        ArrayList<StavkaIzvestaja> listaBaza;
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        ModelTabeleServer mt = (ModelTabeleServer) tblIzvestaj.getModel();

        // Ovde ima 8 slucajeva, odradili smo samo 2 jer nije citljivo, ali znate ostalo
        
        if (cbDanDo.isSelected() && cbDanOd.isSelected() && cbLaboratorija.isSelected()) {

            if (txtDanDo.getText().isEmpty() || txtDanOd.getText().isEmpty()) {
                return;
            }

            try {
                Date danOd = sdf.parse(txtDanOd.getText());
                Date danDo = sdf.parse(txtDanDo.getText());
                Laboratorija lab = (Laboratorija) cmbLaboratorija.getSelectedItem();

                upit = " AND datumvremerezultata BETWEEN '" + new java.sql.Date(danOd.getTime())
                        + "' AND '" + new java.sql.Date(danDo.getTime()) + "' AND "
                        + "laboratorijaid = " + lab.getLaboratorijaId();

                listaBaza = Kontroler.getInstance().vratiIzvestaj(upit);
                mt.dodajIzvestaj(listaBaza);

            } catch (ParseException ex) {
                Logger.getLogger(ServerskaForma.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        if (!cbDanDo.isSelected() && !cbDanOd.isSelected() && !cbLaboratorija.isSelected()) {
            listaBaza = Kontroler.getInstance().vratiIzvestaj(upit);
            mt.dodajIzvestaj(listaBaza);
        }

    }

