    private void btnObrisi1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisi1ActionPerformed

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblAngazovanja.getModel();

        ArrayList<PredajePredmet> angazovanja = mt.getLista();

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.SACUVAJ, angazovanja);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        boolean uspesno = (boolean) so.getOdgovor();
        
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
        } else {
            System.out.println("Ako udjes u server u output i pise ti: "
                    + "Duplicate entry '1-1' for key 'PRIMARY' \n"
                    + "to znaci da pokusavas da dodas 2x isti primarni kljuc za PredajePredmet (angazovanje). "
                    + "\nStavio/la istog nastavnika za isti predmet, a ne mozemo dva puta "
                    + "da unesemo isto! Eto zbog toga se desila greska, kod je inace dobar.");
            JOptionPane.showMessageDialog(this, "Nije sacuvano. POGLEDAJ OUTPUT NA KLIJENTU, "
                    + "AKO TI JE DOBAR KOD, ONDA SAM NAPISAO ZBOG CEGA SE DESILA GRESKA.");
        }

    }//GEN-LAST:event_btnObrisi1ActionPerformed

