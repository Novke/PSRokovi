    private void popuniOpstine() {

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_OPSTINE);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Opstina> opstine = (ArrayList<Opstina>) so.getOdgovor();

        cmbOpstina.removeAllItems();

        for (Opstina opstina : opstine) {
            cmbOpstina.addItem(opstina);
        }

    }

    void dodajTipVakcine(String tipVakcine) {

        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblStavka.getModel();

        if (mt.postojiTipVakcine(tipVakcine)) {
            JOptionPane.showMessageDialog(this, "Jedan tip vakcine se u prijavi moze "
                    + "pojaviti samo jednom!");
            return;
        }

        mt.dodajTipVakcine(tipVakcine);

    }
