    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        if (txtNaziv.getText().isEmpty() || txtOpis.getText().isEmpty()
                || txtDatumVremeOd.getText().isEmpty() || txtDatumVremeDo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy hh:mm:ss");

        try {
            String naziv = txtNaziv.getText();
            String opis = txtOpis.getText();
            Date datumVremeOd = sdf.parse(txtDatumVremeOd.getText());
            Date datumVremeDo = sdf.parse(txtDatumVremeDo.getText());

            if (datumVremeOd.after(datumVremeDo)) {
                JOptionPane.showMessageDialog(this, "Datum i vreme pocetka mora biti "
                        + "pre datuma i vremena kraja dogadjaja!");
                return;
            }

            String tipDogadjaja = (String) cmbTip.getSelectedItem();

            ModelTabeleKlijent mt = (ModelTabeleKlijent) tabelaUcesnika.getModel();

            if (mt.getListaUcesca().size() == 1) {
                JOptionPane.showMessageDialog(this, "Dogadjaj mora imati bar jednog ucesnika "
                        + "pored organizatora!");
                return;
            }

            ArrayList<Ucesce> listaUcesca = mt.getListaUcesca();

            Dogadjaj dog = new Dogadjaj(-1, naziv, opis, datumVremeOd,
                    datumVremeDo, tipDogadjaja, korisnik, listaUcesca);

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ);
            kz.setParametar(dog);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            boolean uspesno = (boolean) so.getOdgovor();
            
            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
            } else {
                JOptionPane.showMessageDialog(this, "Nije uspesno sacuvano...");
            }

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

