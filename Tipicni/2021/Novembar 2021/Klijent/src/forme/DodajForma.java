    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if(txtOpis.getText().isEmpty() || txtBrojSati.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }
        
        String vrstaAngazovanja = (String) cmbVrstaAngazovanja.getSelectedItem();
        String opisPosla = txtOpis.getText();
        int brojSati = Integer.parseInt(txtBrojSati.getText());
        
        //stavio sam samo ovu validaciju za broj sati, treba da bude i manji od ukupnog vremena
        //snimanja, ali toga nema nigde. Jedino da oduzmem datum i vreme do sa datumom i vremenom od
        //ali to znaci da moze bez prestanka da se snima 5 dana (120 sati) ako su datumi tako odvojeni?
        //lose koncipirana napomena, lose

        if (brojSati <= 0) {
            JOptionPane.showMessageDialog(this, "Broj sati mora biti veci od nule!");
        }
        
        Radnik radnik = (Radnik) cmbRadnik.getSelectedItem();
        
        Angazovanje a = new Angazovanje(-1, vrstaAngazovanja, opisPosla, brojSati, null, radnik);

        KlijentskaForma kf = (KlijentskaForma) getParent();
        kf.dodajAngazovanje(a);
        this.dispose();

    }//GEN-LAST:event_btnDodajActionPerformed

