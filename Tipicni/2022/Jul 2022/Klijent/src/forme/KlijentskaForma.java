    void dodajStavku(StavkaProgramskogVodica spv) {
        ModelTabeleStavke mt = (ModelTabeleStavke) tblStavke.getModel();
        
        if(mt.postojiNazivEmisije(spv.getNazivEmisije())){
            JOptionPane.showMessageDialog(this, "Vec postoji ovaj naziv emisije!");
            return;
        }
        
        mt.dodajStavku(spv);
    }
